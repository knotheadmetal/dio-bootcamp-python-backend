# Filtrar lista
numeros = [1, 30, 21, 2, 9, 65, 34] # Lista de números
pares = [numero for numero in numeros if numero % 2 == 0] # Filtrando números pares
print(pares)

# Modificar valores
numeros = [1, 30, 21, 2, 9, 65, 34] # Lista de números
quadrado = [numero**2 for numero in numeros] # Elevando os números ao quadrado
print(quadrado)
