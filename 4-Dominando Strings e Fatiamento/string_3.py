nome = "Rafael da Silva Santos"

print(nome[0])      # Primeiro caractere
print(nome[-2])    # Penúltimo caractere
print(nome[:9])   # Do início até o índice 8
print(nome[10:])  # Do índice 10 até o final
print(nome[10:16])  # Do índice 10 até o 15
print(nome[10:16:2])  # Do índice 10 até o 15, pulando de 2 em 2
print(nome[:])  # A string completa
print(nome[::-1])  # A string de trás para frente
